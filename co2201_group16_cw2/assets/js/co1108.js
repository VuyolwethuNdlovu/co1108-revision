document.getElementById("showAnswer").onclick = function() {
    document.getElementById("answer").style.visibility = "visible";
}

document.getElementById("showExplanation").onclick = function() {
    document.getElementById("explanation").style.visibility = "visible";
}

document.getElementById("hideAnswer").onclick = function() {
    document.getElementById("answer").style.visibility = "hidden";
}

document.getElementById("hideExplanation").onclick = function() {
    document.getElementById("explanation").style.visibility = "hidden";
}